package request.api;

public class Players {
    private int id;
    private String screenName;
    private String gender;
    private int age;

    public int getId() {
        return id;
    }

    public String getScreenName() {
        return screenName;
    }

    public String getGender() {
        return gender;
    }

    public int getAge() {
        return age;
    }
}
